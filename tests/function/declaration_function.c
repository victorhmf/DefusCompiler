#include <stdio.h>
#include <stdlib.h>

//Soma dois números
int soma (int one_parameter, int second_paramater) {

int resultado = 0;
resultado = one_parameter + second_paramater;

return resultado;

}

//Subtrai dois números
int subtracao (int one_parameter, int second_paramater) {

	int resultado = 0;
	resultado = one_parameter - second_paramater;

	return resultado;

}

